function search() {
    const inputEl = document.getElementById("input-el");
    const languageSelect = document.getElementById("language");
    const regionSelect = document.getElementById("region");
    const displayRecipe = document.getElementById("diplayrecipe");

    const searchText = inputEl.value.toLowerCase();
    const selectedLanguage = languageSelect.value;
    const selectedRegion = regionSelect.value;

  
    if (searchText.trim() === "") {
        
        displayRecipe.innerHTML = "Please enter ingredients to search for recipes.";
        return; 
    }

   
    const searchIngredients = searchText.split(' ').map(ingredient => ingredient.trim());

    const filteredRecipes = recipes.filter(recipe => {
        
        const ingredientsMatch = searchIngredients.some(searchIngredient => {
            return recipe.ingredients.some(recipeIngredient =>
                recipeIngredient.toLowerCase().includes(searchIngredient)
            );
        });

        const languageMatch = selectedLanguage === "" || recipe.language === selectedLanguage;
        const regionMatch = selectedRegion === "" || recipe.region === selectedRegion;

        return ingredientsMatch && languageMatch && regionMatch;
    });

    const resultSize = filteredRecipes.length;

    if (filteredRecipes.length > 0) {
        const resultHTML = filteredRecipes.map(recipe => {
            return `
                <div class="recipe">
                <img src="${recipe.image}" alt="${recipe.name}" class="recipe-img">
                    <h2>${recipe.name}</h2>
                    <p class="resultfont"><strong>Ingredients:</strong> ${recipe.ingredients.join(", ")}</p>
                    <p class="resultfont"><strong>Instructions:</strong> ${recipe.instruction}</p>
                </div>
            `;
        }).join("");

        displayRecipe.innerHTML = resultHTML;
    } else {
        displayRecipe.innerHTML = "No recipes found with the specified criteria.";
    }
}





